            <h2>Waspadai Peredaran Uang Palsu saat Tukar Uang Baru di Tepi Jalan.</h2>
            <img src="berita1.jpg" alt="logo" width="500" />
            <p>MALANG - Menjelang Idul Fitri 1445 Hijriyah, Polres Malang mengimbau masyarakat untuk lebih berhati-hati terhadap uang palsu. Peredaran uang palsu perlu diwaspadai terutama ketika akan melakukan penukaran uang.
            Sebagaimana diketahui, jasa penukaran uang resmi yang minim terjadinya pertukaran uang palsu bisa dilakukan di Bank Indonesia (BI). Namun, masih banyak masyarakat yang memanfaatkan jasa penukaran uang tak resmi seperti di pinggir jalan yang saat ini mulai menjamur.
            Kasihumas Polres Malang Ipda Dicka Ermantara mengatakan, untuk mencegah terjadinya peredaran uang palsu, pihaknya telah melakukan imbauan secara preventif maupun preemtif.
            “Kepada masyarakat Kabupaten Malang untuk menukarkan uangnya di bank yang sudah terafiliasi dengan BI. Seperti bank pemerintah maupun swasta,” kata Dicka ketika dikonfirmasi Rabu (27/3/2024).
            Terlebih saat ini BI Malang telah memberlakukan kas keliling untuk melayani penukaran uang baru bagi masyarakat khususnya di Kabupaten Malang. Sehingga hal ini dapat mempermudah masyarakat.
            Selain mencegah terjadinya uang palsu, benefit lain dari menukarkan uang di bank yakni tidak ada biaya penukaran. Seperti halnya di jasa penukaran uang di pinggir jalan, penukar dikenakan biaya per Rp 100 ribunya.
            Berdasarakan penuturan Dicka, hingga saat ini pihaknya belum mendapatkan laporan terkait uang palsu. Namun apabila masyarakat menemui kasus tersebut, segera untuk melaporkannya ke pihak kepolisian.
            “Jika masyarakat mendapati informasi terkait penyebaran uang palsu maupun transaksi uang palsu terutama di media online, langsung dilaporkan ke kami,” urainya.
            Untuk melaporkan melalui call center 110, datang ke Polres Malang maupun polsek terdekat, dan bisa melaporkannya ke media sosial Instagram Polres Malang Official. </p>
